const PROVIDERS = {
  groq: { url: 'https://api.groq.com/openai/v1/chat/completions', key: process.env.GROQ_API_KEY },
  together: { url: 'https://api.together.xyz/v1/chat/completions', key: process.env.TOGETHER_API_KEY },
  openrouter: { url: 'https://openrouter.ai/api/v1/chat/completions', key: process.env.OPENROUTER_API_KEY },
  mistral: { url: 'https://api.mistral.ai/v1/chat/completions', key: process.env.MISTRAL_API_KEY },
};

app.post('/chat', async (req, res) => {
  const { message, provider = 'groq', model = 'llama3-8b-8192' } = req.body;
  const p = PROVIDERS[provider];
  if (!p || !p.key) return res.status(400).json({ error: 'Provider not configured' });

  const response = await fetch(p.url, {
    method: 'POST',
    headers: { 'Authorization': `Bearer ${p.key}`, 'Content-Type': 'application/json' },
    body: JSON.stringify({ model, messages: [{ role: 'user', content: message }] })
  });
  const data = await response.json();
  res.json(data);
});

app.get('/providers', (req, res) => {
  const available = Object.entries(PROVIDERS)
    .filter(([_, p]) => p.key)
    .map(([name]) => name);
  res.json({ providers: available });
});

app.listen(process.env.PORT || 3000, () => console.log('Chatbot API ready'));
EOF
      cat > .env.example << 'EOF'
