scaffold_project() {
  local template_id="$1"
  local project_name="$2"
  local description="${3:-Auto-generated by Repo Builder}"

  log INFO "Scaffolding: $project_name from template $template_id"

  mkdir -p "$project_name"
  cd "$project_name"

  case "$template_id" in
    node-api)
      cat > package.json << EOF
{
  "name": "$project_name",
  "version": "1.0.0",
  "description": "$description",
  "main": "src/index.js",
  "scripts": {
    "start": "node src/index.js",
    "dev": "nodemon src/index.js",
    "test": "jest"
  },
  "dependencies": {
    "express": "^4.18.0",
    "cors": "^2.8.5",
    "helmet": "^7.0.0",
    "dotenv": "^16.0.0"
  },
  "devDependencies": {
    "nodemon": "^3.0.0",
    "jest": "^29.0.0"
  }
}
EOF
      mkdir -p src
      cat > src/index.js << 'EOF'
const express = require('express');
const cors = require('cors');
const helmet = require('helmet');
require('dotenv').config();

const app = express();
app.use(cors());
app.use(helmet());
app.use(express.json());

app.get('/', (req, res) => res.json({ status: 'ok', message: 'API running' }));
app.get('/health', (req, res) => res.json({ uptime: process.uptime() }));

const PORT = process.env.PORT || 3000;
app.listen(PORT, () => console.log(`Server running on port ${PORT}`));
EOF
      ;;

    react-app)
      cat > package.json << EOF
{
  "name": "$project_name",
  "version": "1.0.0",
  "private": true,
  "scripts": {
    "dev": "vite",
    "build": "vite build",
