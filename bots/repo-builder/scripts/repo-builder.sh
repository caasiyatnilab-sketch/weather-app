# ═══════════════════════════════════════════════════════
# Auto-Create GitHub Repos
# ═══════════════════════════════════════════════════════
auto_create_repos() {
  log INFO "Checking for repos to create..."

  # Check if we have a project ideas file
  if [ -f ".github/project-ideas.json" ]; then
    python3 -c "
import json, subprocess, sys

with open('.github/project-ideas.json') as f:
    ideas = json.load(f)

for idea in ideas.get('pending', []):
    name = idea['name']
    desc = idea.get('description', '')
    template = idea.get('template', 'static-site')
    private = idea.get('private', False)

    # Check if repo exists
    result = subprocess.run(['gh', 'repo', 'view', name], capture_output=True)
    if result.returncode != 0:
        visibility = '--private' if private else '--public'
        subprocess.run(['gh', 'repo', 'create', name, visibility, '-d', desc, '--clone'])
        print(f'Created: {name}')
    else:
        print(f'Exists: {name}')

# Move to completed
if 'completed' not in ideas:
    ideas['completed'] = []
ideas['completed'].extend(ideas.get('pending', []))
ideas['pending'] = []

with open('.github/project-ideas.json', 'w') as f:
    json.dump(ideas, f, indent=2)
" 2>/dev/null || log INFO "No pending projects to create"
  fi
}

